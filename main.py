def tree(length):
    if length < 15:
        return
    else:
        t.forward(length)
        t.pensize(2)
        t.pencolor('purple')
        t.circle(2)
        t.pencolor('white')
        t.circle(1)
        t.pencolor('#004260')
        t.left(30)
        tree(3 * length / 4)
        t.right(60)
        t.pencolor('#004240')
        tree(3 * length / 4)
        t.left(30)
        t.backward(length)

