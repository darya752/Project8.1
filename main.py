def koch(ln):
    t.pensize(1)
    t.pencolor('white')
    if ln > 6:
        ln //= 3
        koch(ln)
        t.left(60)
        koch(ln)
        t.right(120)
        koch(ln)
        t.left(60)
        koch(ln)
    else:
        t.fd(ln)
        t.left(60)
        t.fd(ln)
        t.right(120)
        t.fd(ln)
        t.left(60)
        t.fd(ln)

def cir(a):
    if a < 1:
        return
    t.speed(100)
    t.up()
    t.right(6)
    t.down()
    t.circle(30)
    t.up()
    return cir(a*0.9)

